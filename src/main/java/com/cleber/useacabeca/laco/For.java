package com.cleber.useacabeca.laco;

public class For{
	
	public static void main(String[] args) {
		
		for(int lacoFor = 0; lacoFor < 5; lacoFor++) {
			
			System.out.println(lacoFor);
			
		}
	}
}

/*
 * O laço for e o laço while são apenas formas diferentes de uma mesma 
 * estrutura básica de repetição.
 * 
 * Qualquer que seja a estrutura de repetição, ela contém quatro elementos
 * fundamentais: inicialização, condição, corpo e iteração.
 * 
 * Observe que a inicialização, condição e iteração aparecem, entre parênteses,
 * após a palavra reservada “for” e elas são separadas apenas por um
 * ponto-e-vírgula. A instrução ou bloco de instruções que este tipo de laço
 * repete são transcritos a partir da linha subsequente ao seu cabeçalho.
 * 
 */